    private String formatarData(Date data) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        return sdf.format(data);
    }

    private void botaoEditarBTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoEditarBTMouseClicked
        try {
            if (cartaoPesquisaTF.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nenhum paciente procurado!", "Erro", 1);
            } else {
                setEditar(sit);
                if (botaoEditarBT.getText().equals("SALVAR EDIÇÃO")) {
                    new PacienteDAO().atualizarDadosPaciente(new DadoPaciente(Integer.valueOf(cartaoTF.getText()), nomeTF.getText(),
                            emailTF.getText(), logradouroTF.getText(), Integer.valueOf(nroTF.getText()), cidadeTF.getText(),
                            telefoneTF.getText().replaceAll("[^0-9]", ""), formatarData(dataDT.getDate())));
                    sit = !sit;
                    limparCampos();
                }
                if (sit) {
                    botaoEditarBT.setText("SALVAR EDIÇÃO");
                } else {
                    botaoEditarBT.setText("EDITAR");
                    setEditar(sit);
                }
            }
        } catch (ParseException ex) {
            Logger.getLogger(PesquisarPaciente.class.getName()).log(Level.SEVERE, null, ex);
        }
        sit = true;
    }//GEN-LAST:event_botaoEditarBTMouseClicked
    private void limparCampos() {
        cartaoPesquisaTF.setText("");
        cartaoTF.setText("");
        nomeTF.setText("");
        logradouroTF.setText("");
        cidadeTF.setText("");
        nroTF.setText("");
        emailTF.setText("");
        telefoneTF.setText("");
        dataDT.setDate(null);
    }
