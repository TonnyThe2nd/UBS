    private void buscarBTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarBTMouseClicked
        DadoPaciente df = new PacienteDAO().pesquisarPacienteCartao(Integer.valueOf(cartaoPesquisaTF.getText()));
        cartaoTF.setText(String.valueOf(df.getCartaoPaciente()));
        nomeTF.setText(df.getNome());
        emailTF.setText(df.getEmail());
        telefoneTF.setText(df.getTelefone());
        logradouroTF.setText(df.getLogradouro());
        nroTF.setText(String.valueOf(df.getNro()));
        cidadeTF.setText(df.getCidade());
        dataDT.setDate(df.getData_nascimento());
    }//GEN-LAST:event_buscarBTMouseClicked
