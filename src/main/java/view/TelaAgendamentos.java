    private void tabelaAgendamentoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabelaAgendamentoKeyPressed
        tabelaAgendamento.requestFocus();
        if (evt.getKeyCode() == KeyEvent.VK_RIGHT) {
            DefaultTableModel dtm = (DefaultTableModel) tabelaAgendamento.getModel();
            if(dtm.getRowCount()>0){
                nomePacienteLB.setText(tabelaAgendamento.getValueAt(0, 0).toString());
                dtm.removeRow(0);
            }else{
                nomePacienteLB.setText("Nenhum paciente na fila!");
            }
            
        }
    }//GEN-LAST:event_tabelaAgendamentoKeyPressed

    private void setTable() {
        AgendamentoDAO user = new AgendamentoDAO();
        List<AgendamentoPaciente> listaPacientes = user.getAllAgendamento();

        DefaultTableModel dtm = (DefaultTableModel) tabelaAgendamento.getModel();
        dtm.setRowCount(0);
        for (AgendamentoPaciente paciente : listaPacientes) {
            Object[] ob = {paciente.getNomePaciente()};
            dtm.addRow(ob);
        }
        nomePacienteLB.setText(tabelaAgendamento.getValueAt(0, 0).toString());
        dtm.removeRow(0);
    }

